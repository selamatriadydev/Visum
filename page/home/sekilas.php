          <strong>Visum Et Repertum</strong> adalah alat bukti dalam proses peradilan yang harus memenuhi standar penulisan rekam medis dan juga harus memenuhi hal-hal yang diisyaratkan dalam sistim peradilan. Visum Et Repertum merupakan layanan utama kedokteran forensik klinik yang merupakan aplikasi pelayanan medis klinis pada korban yang mengalami cidera dengan melibatkan bukti-bukti forensic yang cukup. Istilah Visum et Repertum ini dapat ditemukan dalam lembaran Negara tahun 1937 Nomor : 350 Pasal I yang terjemahannya : Visa et Reperta pada Dokter yang dibuat baik atas sumpah Dokter yang diucapkan pada waktu menyelesaikan pelajarannya di Negeri Belanda atau Indonesia, maupun atas sumpah khusus seperti tercantum dalam pasal 2, mempunyai daya bukti yang syah dalam perkara pidana selama visa et Reperta tersebut berisi keterangan mengenai hal hal yang diamati oleh Dokter itu pada benda-benda yang diperiksa. Setelah KUHAP berlaku ketentuan mengenai Visum et Repertum masih tetap digunakan, karena isi dari Lembaran Negara tersebut tidak bertentangan KUHAP, terutama karena di dalam KUHAP sendiri tidak menjelaskan apa yang dimaksud Visum et Repertum. Mengenai istilah Visum et Repertum semula simpang siur, sehingga kemudian Menteri Kehakiman dalam peraturan Nomor : M. 04.UM.01.06 tahun 1983 pasal 10 menyatakan bahwa hasil pemeriksaan Ilmu Kedokteran Kehakiman disebut Visum et Repertum.Oleh karena itu keterangan ahli/keterangan hasil pemeriksaan Ilmu Kedokteran Kehakiman seperti dimaksud KUHAP tidak lain adalah Visum et Repertum.
