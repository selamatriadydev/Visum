          Permintaan <strong>Visum Et Repertum</strong> diajukan oleh penyidik kepolisian kepada Dokter Kehakiman atau Dokter dokter lainnya, tapi ada beberapa kendalam apabila permintaan Visum et Repertum akan diajukan kepada Dokter ahli kedokteran kehakiman, karena dokter tersebut hanya berada di ibu kota propinsi yang memiliki Universitas dengan fakultas kedokterannya. 
          <hr>
          Permintaan <strong>Visum Et Repertum</strong> dalam pelaksanaannya adalah: 
          <ol><strong>></strong> Memproritaskan dokter pemerintah, ditempat dinasnya (bukan tempat praktek partikelir).</ol>
           <ol><strong>></strong> Kalau tidak ada maka permohonan tersebut ditujukan ditempat yang ada fasilitas rumah sakit umum/Fakultas kedokteran dan permintaan tersebut ditujukan kepada bagian yang sesuai dengan tindak kejahatan yang dialaminya. </ol>
           <ol><strong>></strong> Jika di daerah tempat kejadian tindak pidana terjadi tidak memiliki fasilitas tersebut maka permintaan ditujukan kepada dokter pemerintah di puskesmas atau Dokter TNI terutama lebih tepat jika ditujukan kepada dokter Polri. Bila hal ini tidak memungkinkan, baru dimintakan ke Dokter swsata. </ol>
            <ol><strong>></strong> Korban yang akan di visum, baik hidup maupun mati harus diantar sendiri oleh petugas Polri, disertai surat permintaan untuk dilakukan Visum et Repertum. </ol>
            <hr>
              <strong>Visum Et Repertum</strong> sebagai salah satu alat bukti dalam proses peradilan penulisan Visum Et Repertum haruslah didesain dan diformat sesuai ketentuan suatu Visum Et Repertum.
              <br> Adapaun format dari Visum Et Repertum adalah :
                <ol><strong>1.</strong> Pro Justitia </ol>
                <ol><strong>2.</strong> Pendahuluan Pada bagian ini harus ditulis nama rumah sakit secara jelas berikut alamatnya, data subyek yang diperiksa, data peminta pemeriksaan, data dokter pemeriksa. </ol>
                 <ol><strong>3.</strong> Pemberitaan Pada bagian ini haruslah memuat tanda-tanda vital, luka dan lokasi lukanya, jens luka, karakteristik dan ukuran, pengobatan atau perawatan yang diberikan, hasil pengobatan yang diberikan kepada subyek. </ol>
                 <ol><strong>4.</strong> Kesimpulan Pada bagian ini adalah bagian yang penting dimana dicantumkan mengenai jenis luka, jenis kekerasan, kualifikasi luka Penutup
